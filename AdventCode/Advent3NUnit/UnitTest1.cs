    public class Advent3
    {

        public static int CalcMostCommonBit(List<int> input, int position)
        {
            int one_count = 0;
            int zero_count = 0;

            for (int n=0; n<input.Count; n++)
            {
                if ((input[n] & 1<<position) != 0)
                    one_count ++;
                else
                    zero_count ++;
            }

            if (one_count > zero_count)
                return 1;
            else
                return 0;
        }

        public static int CalcLeastCommonBit(List<int> input, int position)
        {
            if (CalcMostCommonBit(input, position) == 1)
                return 0;
            else
                return 1;
        }

        public static int CalcMaxBits(List<int> input)
        {
            int max = input.Max();
            return Convert.ToInt32(Math.Log2(max)) + 1;
        }

        public static int CalcGamma(List<int> input)
        {
            int gamma = 0;

            for (int n=0; n<CalcMaxBits(input); n++)
            {
                gamma = gamma | (CalcMostCommonBit(input, n) << n);
            }

            return gamma;
        }

        public static int CalcEpsilon(List<int> input)
        {
            int epsilon = 0;

            for (int n=0; n<CalcMaxBits(input); n++)
            {
                epsilon = epsilon | (CalcLeastCommonBit(input, n) << n);
            }

            return epsilon;
        }

    }

