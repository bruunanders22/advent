        [Test]
        public void TestMostCommonBit()
        {
            List <int> input = new List<int>();
            input.Add(1);
            input.Add(1);
            input.Add(1);
            input.Add(1);
            input.Add(1);
            input.Add(1);
            input.Add(2);
            input.Add(4);
            input.Add(8);
            input.Add(16);
            input.Add(32);

            Assert.AreEqual(1, Advent3.CalcMostCommonBit(input, 0));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 1));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 2));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 3));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 4));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 5));
        }

        [Test]
        public void TestGamma()
        {
            List <int> input = new List<int>();
            input.Add(65);
            input.Add(65);
            input.Add(0);

            Assert.AreEqual(1, Advent3.CalcMostCommonBit(input, 6));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 5));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 4));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 3));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 2));
            Assert.AreEqual(0, Advent3.CalcMostCommonBit(input, 1));
            Assert.AreEqual(1, Advent3.CalcMostCommonBit(input, 0));

            Assert.AreEqual(7, Advent3.CalcMaxBits(input));

            Assert.AreEqual(0b1000001, Advent3.CalcGamma(input));
        }

        [Test]
        public void TestEpsilon()
        {
            List <int> input = new List<int>();
            input.Add(65);
            input.Add(65);
            input.Add(0);

            Assert.AreEqual(0b0111110, Advent3.CalcEpsilon(input));
